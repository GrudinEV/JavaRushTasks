/* 
Произведение 10 чисел
*/

public class Solution {
    public static void main(String[] args) {
        int n = 2;
        for (int i = 3; i <= 10; i++) {
            n = n * i;
        }
        System.out.println(n);
        //напишите тут ваш код

    }
}
